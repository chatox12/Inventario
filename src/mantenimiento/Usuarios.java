    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        Insertar insertar = new Insertar();
        String datos = this.txtNombre.getText();
        String direccion = this.txtDireccion.getText();
        String telefono = this.txtTelefono.getText();
        String user = this.txtUsuario.getText();
        String pass = this.txtContrasena.getText();
        if ("".equals(datos) && "".equals(direccion) && "".equals(telefono) &&
             "".equals(user) && "".equals(pass)) {
            JOptionPane.showMessageDialog(null, "Debe Ingresar un Usuario");
            
        }
        else{
            if(insertar.insertar_usuario(datos, direccion, telefono, user, pass, cbotipo.getSelectedIndex()) == 1){
            JOptionPane.showMessageDialog(null, "Usuario Agregado con Exito");
            this.dispose();
        }
        else{
            JOptionPane.showMessageDialog(null, "ERROR Al Guardar el Usuario");
            }
            
        
        }
        
        
    }//GEN-LAST:event_btnGuardarActionPerformed


