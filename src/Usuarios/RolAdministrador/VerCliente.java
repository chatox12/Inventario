      String comparar(String cod)
    {
        String cant="";
        try {
            Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery("SELECT * FROM clientes WHERE pro_nombre='"+cod+"'");
            while(rs.next())
            {
                cant=rs.getString(4);
            }
            
        } catch (SQLException ex) {
            System.out.println("ERROR al cargar los datos"+ex);
        }
        return cant;
        
    }
     void cargarlistaproductos(String dato){
        String [] Titulo = {"idProducto","Nombre","Telefono","Direccion"};
        tabla=new DefaultTableModel(null,Titulo);
    String []Registro= new String[4];
    String mostrar="SELECT * FROM clientes WHERE CONCAT (cli_nombre,cli_telefono,cli_direccion) LIKE '%"+dato+"%'"; 
    Statement st;
        try {
            st = cn.createStatement();
            ResultSet rs =st.executeQuery(mostrar);
            while(rs.next())
            {
                Registro[0]=rs.getString("idclientes");
                Registro[1]=rs.getString("cli_nombre");
                Registro[2]=rs.getString("cli_telefono");
                Registro[3]=rs.getString("cli_direccion");
                
                tabla.addRow(Registro);
            }
            tblClientes.setModel(tabla);
        } catch (SQLException ex) {
            System.out.println("ERROR... Al Cargar los Datos"+ex);
        }
    
    
    
    
    }
